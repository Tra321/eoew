package model;

public class Dormitory {
    private String dormitoryID;
    private String dormitoryBuild;
    private int dormitoryFloor;

    public String getDormitoryID() {
        return dormitoryID;
    }

    public void setDormitoryID(String dormitoryID) {
        this.dormitoryID = dormitoryID;
    }

    public String getDormitoryBuild() {
        return dormitoryBuild;
    }

    public void setDormitoryBuild(String dormitoryBuild) {
        this.dormitoryBuild = dormitoryBuild;
    }

    public int getDormitoryFloor() {
        return dormitoryFloor;
    }

    public void setDormitoryFloor(int dormitoryFloor) {
        this.dormitoryFloor = dormitoryFloor;
    }
}
